-- 026: 4번째 항목(토론/extra) 기본 점수 10 → 100(만점)
-- 토론은 100점 만점 점수. 가중치 10%면 만점(100)×10% = 최종 10점 기여.
-- "이번 학기 모두 토론 10점 부여" = 토론 점수를 만점(100)으로 두는 것.
-- 025 이후 실행. 재실행 안전.
-- ===================================================================

-- 1. 신규 행 기본값을 만점(100)으로
alter table enrollments alter column extra set default 100;

-- 2. 기존 기본값(10)으로 채워진 행을 만점(100)으로 전환
--    (토론 채점 전이라 10은 모두 자동 기본값 → 안전. 실제 점수 입력분은 10이 아니면 보존)
update enrollments set extra = 100 where extra = 10;
